"""
Real ablation of the four representative 7k configurations.

Supports two modes controlled by --mode (default: forward):

  forward   reads the real irace-style forward trajectory generated by the
            Java validation runner. The path is selected on the seen split and
            visualized on the unseen split with a waterfall chart.
            Requires forward-ablation experiment bundles.

  knockout  Component knockout: starting from the target, one block at a time
            is reset to the NSGA-II default to measure the HV loss.
            Requires knockout-ablation experiment bundles.

Usage:
  python experiments/rq4_validation/generate_validation_ablation.py
  python experiments/rq4_validation/generate_validation_ablation.py --mode knockout
"""

from __future__ import annotations

import argparse

import matplotlib
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

from rq4_validation_utils import (
    GENERATED_DIR,
    OUTPUT_DIR,
    aggregate_by_split,
    draw_placeholder_figure,
    load_validation_bundle,
)

matplotlib.rcParams.update(
    {
        "font.family": "serif",
        "font.size": 10,
        "axes.linewidth": 0.8,
        "xtick.major.width": 0.8,
        "ytick.major.width": 0.8,
        "axes.grid": True,
        "grid.linewidth": 0.35,
        "grid.alpha": 0.30,
        "grid.color": "#bbbbbb",
    }
)

FIGURE_BASENAME = OUTPUT_DIR / "validation_ablation_rq4"
RECOMMENDATION_FIGURE_BASENAME = OUTPUT_DIR / "validation_ablation_recommendations_rq4"
SUMMARY_CSV = GENERATED_DIR / "validation_ablation_rq4.csv"

ABLATION_CONFIGS = [
    ("RE", "Complete-RE3D"),
    ("RE", "Extreme-RE3D"),
    ("RWA", "Complete-RWA3D"),
    ("RWA", "Extreme-RWA3D"),
]

RECOMMENDED_ABLATION_CONFIGS = [
    ("RE", "Extreme-RE3D"),
    ("RWA", "Complete-RWA3D"),
]

COLORS = {
    "Archive": "#1b9e77",
    "Crossover": "#d95f02",
    "Mutation": "#7570b3",
}

RESET_LABELS = ["ArchiveReset", "CrossoverReset", "MutationReset"]


# ---------------------------------------------------------------------------
# Forward (irace-style) ablation
# ---------------------------------------------------------------------------

def forward_color(label: str) -> str:
    lowered = label.lower()
    if label == "Default":
        return "#888888"
    if label == "Target":
        return "#333333"
    if "algorithmresult" in lowered or "archive" in lowered:
        return COLORS["Archive"]
    if "crossover" in lowered:
        return COLORS["Crossover"]
    if "mutation" in lowered:
        return COLORS["Mutation"]
    if "selection" in lowered:
        return "#4c78a8"
    if "offspring" in lowered or "createinitialsolutions" in lowered:
        return "#8c6d31"
    return "#999999"


def format_forward_label(label: str) -> str:
    if label in {"Default", "Target"} or len(label) <= 18:
        return label
    return label.replace("+", "+\n")


def compact_forward_label(label: str) -> str:
    lowered = label.lower()
    if label == "Default":
        return "Default"
    if label == "Target":
        return "Target"
    if "algorithmresult" in lowered or "archive" in lowered:
        return "+ Archive"
    if label == "crossoverProbability":
        return "+ Cross. prob."
    if label == "crossoverRepairStrategy":
        return "+ Cross. repair"
    if "crossover" in lowered:
        return "+ Crossover"
    if label == "mutationProbabilityFactor":
        return "+ Mut. rate"
    if label == "mutationRepairStrategy":
        return "+ Mut. repair"
    if "mutation" in lowered:
        return "+ Mutation"
    if "selection" in lowered:
        return "+ Selection"
    if "offspring" in lowered:
        return "+ Offspring"
    if "createinitialsolutions" in lowered:
        return "+ Init"
    return "+ Parameter"


def run_forward_mode() -> pd.DataFrame:
    """Load forward-ablation bundles and read the real selected trajectory."""
    rows: list[dict[str, object]] = []

    for suite, config_tag in ABLATION_CONFIGS:
        bundle = load_validation_bundle(suite, ablation_base_tag=config_tag, ablation_mode="forward")
        if bundle is None:
            print(f"  Missing forward-ablation bundle for {config_tag} on {suite}")
            continue

        aggregated = aggregate_by_split(bundle["summary"])
        unseen_hv = aggregated[
            (aggregated["Split"] == "unseen") & (aggregated["IndicatorName"] == "HV")
        ][["Algorithm", "SuiteMedian"]]
        trajectory = bundle.get("trajectory")
        if trajectory is None or trajectory.empty:
            print(f"  Missing forward trajectory metadata for {config_tag} on {suite}")
            continue

        hv_lookup = dict(zip(unseen_hv["Algorithm"], unseen_hv["SuiteMedian"]))
        trajectory = trajectory.sort_values("step").reset_index(drop=True)

        previous_hv = None
        for _, row in trajectory.iterrows():
            algorithm = row["algorithm"]
            hv = hv_lookup.get(algorithm)
            if hv is None:
                continue
            delta = 0.0 if previous_hv is None else hv - previous_hv
            rows.append(
                {
                    "Suite": suite,
                    "Config": config_tag,
                    "Step": int(row["step"]),
                    "Algorithm": algorithm,
                    "Block": str(row["label"]),
                    "ChangedParameters": str(row.get("changed_parameters", "")),
                    "HV": hv,
                    "DeltaHV": delta,
                    "SeenHV": float(row["seen_hv"]),
                    "SeenEP": float(row["seen_ep"]),
                    "Evaluated": bool(row["evaluated"]),
                }
            )
            previous_hv = hv

    frame = pd.DataFrame(rows)
    return frame


def draw_forward_figure(frame: pd.DataFrame) -> None:
    """Draw a 2x2 waterfall figure showing the selected forward-ablation path."""
    fig, axes = plt.subplots(2, 2, figsize=(11, 8.2))

    for ax, (suite, config_tag) in zip(axes.flat, ABLATION_CONFIGS):
        config_frame = frame[
            (frame["Suite"] == suite) & (frame["Config"] == config_tag)
        ].sort_values("Step")

        if config_frame.empty:
            ax.set_title(f"{suite}: {config_tag} (no data)", fontsize=10)
            continue

        labels = [format_forward_label(label) for label in config_frame["Block"].tolist()]
        hvs = config_frame["HV"].tolist()
        deltas = config_frame["DeltaHV"].tolist()

        bar_colors = [forward_color(label) for label in config_frame["Block"].tolist()]

        x = np.arange(len(labels))
        ax.bar(x, hvs, color=bar_colors, alpha=0.88, edgecolor="white", linewidth=0.5)

        for i, (hv, delta) in enumerate(zip(hvs, deltas)):
            if i > 0 and delta != 0:
                sign = "+" if delta > 0 else ""
                ax.text(
                    i, hv + 0.001, f"{sign}{delta:.4f}",
                    ha="center", va="bottom", fontsize=7.5, color="#333333",
                )

        ax.set_xticks(x)
        ax.set_xticklabels(labels, rotation=30, ha="right", fontsize=8.5)
        ax.set_title(f"{suite}: {config_tag}", fontsize=10)
        ax.tick_params(which="both", direction="in")
        ax.spines["top"].set_visible(False)
        ax.spines["right"].set_visible(False)
        ax.grid(axis="x", visible=False)

    axes[0, 0].set_ylabel("Median HV (unseen split)", fontsize=9.5)
    axes[1, 0].set_ylabel("Median HV (unseen split)", fontsize=9.5)

    fig.suptitle(
        "Forward ablation path selected on seen and evaluated on unseen for the four representative 7k configurations",
        fontsize=11, y=1.00,
    )
    plt.tight_layout()

    for ext in ("png", "pdf"):
        fig.savefig(FIGURE_BASENAME.with_suffix(f".{ext}"), dpi=300, bbox_inches="tight")
        print(f"Saved: {FIGURE_BASENAME.with_suffix(f'.{ext}')}")
    plt.close(fig)


def draw_recommendation_forward_figure(frame: pd.DataFrame) -> None:
    """Draw the manuscript-focused forward paths for the final recommendations."""
    fig, axes = plt.subplots(1, 2, figsize=(10.6, 4.3))

    for ax, (suite, config_tag) in zip(axes, RECOMMENDED_ABLATION_CONFIGS):
        config_frame = frame[
            (frame["Suite"] == suite) & (frame["Config"] == config_tag)
        ].sort_values("Step")

        if config_frame.empty:
            ax.set_title(f"{suite}: {config_tag} (no data)", fontsize=10)
            continue

        labels = [compact_forward_label(label) for label in config_frame["Block"].tolist()]
        hvs = config_frame["HV"].to_numpy(dtype=float)
        baseline = hvs[0]
        gains = hvs - baseline
        colors = [forward_color(label) for label in config_frame["Block"].tolist()]
        x = np.arange(len(labels))

        ax.axhline(0.0, color="#555555", linewidth=0.8, linestyle="--", zorder=0)
        ax.plot(x, gains, color="#222222", linewidth=1.2, zorder=1)
        ax.scatter(x, gains, s=58, color=colors, edgecolor="white", linewidth=0.6, zorder=2)

        largest_steps = (
            config_frame.assign(abs_delta=config_frame["DeltaHV"].abs())
            .iloc[1:]
            .sort_values("abs_delta", ascending=False)
            .head(2)
        )
        annotate_steps = set(largest_steps["Step"].astype(int).tolist())
        annotate_steps.add(int(config_frame["Step"].iloc[-1]))

        y_range = max(float(gains.max() - gains.min()), 1e-4)
        for _, row in config_frame.iterrows():
            step = int(row["Step"])
            if step == 0 or step not in annotate_steps:
                continue
            value = float(row["HV"] - baseline)
            offset = 0.045 * y_range if value >= 0 else -0.08 * y_range
            label = f"{value:+.4f}"
            ax.text(
                step,
                value + offset,
                label,
                ha="center",
                va="bottom" if value >= 0 else "top",
                fontsize=7.5,
                color="#222222",
            )

        y_margin = max(0.14 * y_range, 3e-4)
        ax.set_ylim(float(gains.min() - y_margin), float(gains.max() + y_margin))
        ax.set_xticks(x)
        ax.set_xticklabels(labels, rotation=35, ha="right", fontsize=8)
        ax.set_title(f"{suite}: {config_tag}", fontsize=10.5)
        ax.set_xlabel("Cumulative forward step", fontsize=9.5)
        ax.tick_params(which="both", direction="in")
        ax.spines["top"].set_visible(False)
        ax.spines["right"].set_visible(False)
        ax.grid(axis="x", visible=False)

    axes[0].set_ylabel("HV gain over default on unseen split", fontsize=9.5)
    axes[1].set_ylabel("HV gain over default on unseen split", fontsize=9.5)
    fig.suptitle(
        "Forward ablation of the final deployment recommendations",
        fontsize=11,
        y=1.02,
    )
    plt.tight_layout()

    for ext in ("png", "pdf"):
        fig.savefig(
            RECOMMENDATION_FIGURE_BASENAME.with_suffix(f".{ext}"),
            dpi=300,
            bbox_inches="tight",
        )
        print(f"Saved: {RECOMMENDATION_FIGURE_BASENAME.with_suffix(f'.{ext}')}")
    plt.close(fig)


# ---------------------------------------------------------------------------
# Knockout ablation (original approach)
# ---------------------------------------------------------------------------

def run_knockout_mode() -> pd.DataFrame:
    """Load knockout-ablation bundles and compute relative HV changes."""
    rows: list[dict[str, object]] = []

    for suite, config_tag in ABLATION_CONFIGS:
        ablation_bundle = load_validation_bundle(
            suite, ablation_base_tag=config_tag, ablation_mode="knockout"
        )
        if ablation_bundle is None:
            print(f"  Missing knockout-ablation bundle for {config_tag} on {suite}")
            continue

        ablation_aggregated = aggregate_by_split(ablation_bundle["summary"])
        unseen_hv = ablation_aggregated[
            (ablation_aggregated["Split"] == "unseen")
            & (ablation_aggregated["IndicatorName"] == "HV")
        ][["Algorithm", "SuiteMedian"]]

        base_value = unseen_hv[unseen_hv["Algorithm"] == config_tag]["SuiteMedian"]
        if base_value.empty:
            continue
        base_hv = float(base_value.iloc[0])

        for variant in [config_tag] + [f"{config_tag}-{r}" for r in RESET_LABELS]:
            hv = unseen_hv[unseen_hv["Algorithm"] == variant]["SuiteMedian"]
            if hv.empty:
                continue
            value = float(hv.iloc[0])
            relative_change = 100.0 * (value - base_hv) / base_hv if base_hv != 0 else 0.0
            rows.append({
                "Suite": suite, "Config": config_tag,
                "Variant": variant, "HV": value,
                "RelativeHVChangePercent": relative_change,
            })

    return pd.DataFrame(rows)


def draw_knockout_figure(frame: pd.DataFrame) -> None:
    """Draw a 2x2 bar chart of knockout ablation results."""
    fig, axes = plt.subplots(2, 2, figsize=(10.5, 7.8), sharey=True)

    for ax, (suite, config_tag) in zip(axes.flat, ABLATION_CONFIGS):
        config_frame = frame[
            (frame["Suite"] == suite) & (frame["Config"] == config_tag)
        ].copy()
        config_frame = config_frame[config_frame["Variant"] != config_tag].copy()
        config_frame["Label"] = config_frame["Variant"].str.replace(
            f"{config_tag}-", "", regex=False
        )

        bar_colors = [
            COLORS.get(label.replace("Reset", ""), "#999999")
            for label in config_frame["Label"]
        ]
        ax.bar(
            config_frame["Label"],
            config_frame["RelativeHVChangePercent"],
            color=bar_colors, alpha=0.88,
        )
        ax.axhline(0.0, color="#444444", linewidth=0.9)
        ax.set_title(f"{suite}: {config_tag}", fontsize=10)
        ax.set_xlabel("Reset block", fontsize=9.5)
        ax.tick_params(which="both", direction="in")
        ax.spines["top"].set_visible(False)
        ax.spines["right"].set_visible(False)
        ax.grid(axis="x", visible=False)

    axes[0, 0].set_ylabel("Relative HV change\non unseen split (%)", fontsize=9.5)
    axes[1, 0].set_ylabel("Relative HV change\non unseen split (%)", fontsize=9.5)

    fig.suptitle(
        "Knockout ablation of the four representative 7k configurations",
        fontsize=11, y=1.00,
    )
    plt.tight_layout()

    for ext in ("png", "pdf"):
        fig.savefig(FIGURE_BASENAME.with_suffix(f".{ext}"), dpi=300, bbox_inches="tight")
        print(f"Saved: {FIGURE_BASENAME.with_suffix(f'.{ext}')}")
    plt.close(fig)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(
        description="Real ablation analysis of the representative 7k configurations."
    )
    parser.add_argument(
        "--mode",
        choices=["forward", "knockout"],
        default="forward",
        help="Ablation mode: 'forward' (irace-style, default) or 'knockout' (block reset).",
    )
    args = parser.parse_args()

    if args.mode == "forward":
        print("Running forward (irace-style) ablation analysis ...")
        frame = run_forward_mode()
        frame.to_csv(SUMMARY_CSV, index=False)
        if frame.empty:
            draw_placeholder_figure(
                FIGURE_BASENAME,
                "Forward-ablation bundles not found",
                "Run the validation with --ablation-mode forward first.",
            )
        else:
            draw_forward_figure(frame)
            draw_recommendation_forward_figure(frame)
    else:
        print("Running knockout ablation analysis ...")
        frame = run_knockout_mode()
        frame.to_csv(SUMMARY_CSV, index=False)
        if frame.empty:
            draw_placeholder_figure(
                FIGURE_BASENAME,
                "Knockout-ablation bundles not found",
                "Run the validation with --ablation-mode knockout first.",
            )
        else:
            draw_knockout_figure(frame)


if __name__ == "__main__":
    main()
