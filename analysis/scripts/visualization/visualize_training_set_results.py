#!/usr/bin/env python3
"""
Visualize Pareto fronts from TrainingSet execution results.

This script reads CSV files generated by TrainingSetRunner and creates
visualizations of the obtained Pareto fronts, optionally comparing them
with reference fronts.

Usage:
    python visualize_training_set_results.py <results_dir> [options]

Example:
    python visualize_training_set_results.py results/ZDT --grid --save fronts.png
    python visualize_training_set_results.py results/DTLZ3D --interactive
    python visualize_training_set_results.py results/RE3D --interactive --save fronts.html
"""

import argparse
import os
import sys
from pathlib import Path
from typing import Dict, Optional, Tuple

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

# Optional: plotly for interactive plots
try:
    import plotly.graph_objects as go
    from plotly.subplots import make_subplots
    PLOTLY_AVAILABLE = True
except ImportError:
    PLOTLY_AVAILABLE = False


def load_front(filepath: str) -> np.ndarray:
    """Load a Pareto front from a CSV file."""
    return pd.read_csv(filepath, header=None).values


def load_results(results_dir: str) -> Dict[str, Dict[str, np.ndarray]]:
    """
    Load all results from a results directory.
    
    Expected structure:
        results_dir/
            problem1/
                FUN.csv
                VAR.csv
            problem2/
                FUN.csv
                VAR.csv
            ...
            manifest.csv (optional)
    
    Returns:
        Dictionary mapping problem names to their fronts and reference fronts.
    """
    results = {}
    results_path = Path(results_dir)
    
    # Check for manifest file
    manifest_path = results_path / "manifest.csv"
    if manifest_path.exists():
        manifest = pd.read_csv(manifest_path)
        for _, row in manifest.iterrows():
            problem_name = row['problem']
            fun_path = results_path / problem_name / "FUN.csv"
            ref_path = row.get('reference_front', None)
            
            if fun_path.exists():
                ref_front = None
                if ref_path and isinstance(ref_path, str) and os.path.exists(ref_path):
                    ref_front = load_front(ref_path)
                results[problem_name] = {
                    'front': load_front(str(fun_path)),
                    'reference': ref_front
                }
    else:
        # Auto-discover problem directories
        for problem_dir in sorted(results_path.iterdir()):
            if problem_dir.is_dir():
                fun_path = problem_dir / "FUN.csv"
                if fun_path.exists():
                    results[problem_dir.name] = {
                        'front': load_front(str(fun_path)),
                        'reference': None
                    }
    
    return results


def plot_2d_front(ax: plt.Axes, front: np.ndarray, reference: Optional[np.ndarray] = None,
                  title: str = "", color: str = 'blue', alpha: float = 0.7) -> None:
    """Plot a 2D Pareto front on the given axes."""
    if reference is not None:
        # Sort reference front by first objective for proper line plotting
        sorted_ref = reference[reference[:, 0].argsort()]
        ax.plot(sorted_ref[:, 0], sorted_ref[:, 1], c='gray', alpha=0.5, 
                linewidth=0.8, label='Reference front')
    
    ax.scatter(front[:, 0], front[:, 1], c=color, alpha=alpha, s=20, label='Front')
    
    ax.set_xlabel('$f_1$')
    ax.set_ylabel('$f_2$')
    ax.set_title(title)
    ax.legend(loc='best', fontsize=8)
    ax.grid(True, alpha=0.3)


def plot_3d_front(ax: plt.Axes, front: np.ndarray, reference: Optional[np.ndarray] = None,
                  title: str = "", color: str = 'blue', alpha: float = 0.7) -> None:
    """Plot a 3D Pareto front on the given axes."""
    if reference is not None:
        ax.scatter(reference[:, 0], reference[:, 1], reference[:, 2], 
                   c='gray', alpha=0.3, s=3, label='Reference front')
    
    ax.scatter(front[:, 0], front[:, 1], front[:, 2], c=color, alpha=alpha, s=20, label='Front')
    
    ax.set_xlabel('$f_1$')
    ax.set_ylabel('$f_2$')
    ax.set_zlabel('$f_3$')
    ax.set_title(title)
    ax.legend(loc='best', fontsize=8)


def visualize_grid(results: Dict[str, Dict[str, np.ndarray]], 
                   output_path: Optional[str] = None,
                   figsize: Tuple[int, int] = None) -> None:
    """Create a grid of plots, one per problem."""
    n_problems = len(results)
    if n_problems == 0:
        print("No results to visualize.")
        return
    
    # Determine grid dimensions
    n_cols = min(3, n_problems)
    n_rows = (n_problems + n_cols - 1) // n_cols
    
    # Determine if 2D or 3D
    first_front = next(iter(results.values()))['front']
    is_3d = first_front.shape[1] >= 3
    
    if figsize is None:
        figsize = (5 * n_cols, 4 * n_rows)
    
    if is_3d:
        fig = plt.figure(figsize=figsize)
    else:
        fig, axes = plt.subplots(n_rows, n_cols, figsize=figsize)
        if n_problems == 1:
            axes = np.array([[axes]])
        elif n_rows == 1:
            axes = axes.reshape(1, -1)
        elif n_cols == 1:
            axes = axes.reshape(-1, 1)
    
    colors = plt.cm.tab10(np.linspace(0, 1, n_problems))
    
    for idx, (problem_name, data) in enumerate(results.items()):
        row, col = idx // n_cols, idx % n_cols
        
        if is_3d:
            ax = fig.add_subplot(n_rows, n_cols, idx + 1, projection='3d')
            plot_3d_front(ax, data['front'], data['reference'], 
                         title=problem_name, color=colors[idx])
        else:
            ax = axes[row, col]
            plot_2d_front(ax, data['front'], data['reference'], 
                         title=problem_name, color=colors[idx])
    
    # Hide empty subplots
    if not is_3d:
        for idx in range(n_problems, n_rows * n_cols):
            row, col = idx // n_cols, idx % n_cols
            axes[row, col].set_visible(False)
    
    plt.tight_layout()
    
    if output_path:
        plt.savefig(output_path, dpi=150, bbox_inches='tight')
        print(f"Saved plot to {output_path}")
    
    plt.show()


def visualize_combined(results: Dict[str, Dict[str, np.ndarray]], 
                       output_path: Optional[str] = None,
                       figsize: Tuple[int, int] = (10, 8)) -> None:
    """Create a single plot with all fronts overlaid."""
    if len(results) == 0:
        print("No results to visualize.")
        return
    
    first_front = next(iter(results.values()))['front']
    is_3d = first_front.shape[1] >= 3
    
    fig = plt.figure(figsize=figsize)
    
    if is_3d:
        ax = fig.add_subplot(111, projection='3d')
    else:
        ax = fig.add_subplot(111)
    
    colors = plt.cm.tab10(np.linspace(0, 1, len(results)))
    
    for idx, (problem_name, data) in enumerate(results.items()):
        front = data['front']
        if is_3d:
            ax.scatter(front[:, 0], front[:, 1], front[:, 2], 
                      c=[colors[idx]], alpha=0.7, s=20, label=problem_name)
        else:
            ax.scatter(front[:, 0], front[:, 1], 
                      c=[colors[idx]], alpha=0.7, s=20, label=problem_name)
    
    ax.set_xlabel('$f_1$')
    ax.set_ylabel('$f_2$')
    if is_3d:
        ax.set_zlabel('$f_3$')
    
    ax.legend(loc='best', fontsize=8)
    ax.set_title('All Pareto Fronts')
    ax.grid(True, alpha=0.3)
    
    plt.tight_layout()
    
    if output_path:
        plt.savefig(output_path, dpi=150, bbox_inches='tight')
        print(f"Saved plot to {output_path}")
    
    plt.show()


def visualize_interactive(results: Dict[str, Dict[str, np.ndarray]],
                          output_path: Optional[str] = None,
                          show_reference: bool = True) -> None:
    """
    Create interactive visualization using Plotly.
    
    Supports both 2D and 3D fronts with rotation, zoom, and hover information.
    Reference fronts are shown as semi-transparent surfaces/lines.
    """
    if not PLOTLY_AVAILABLE:
        print("Plotly not available. Install with: pip install plotly")
        return
    
    if len(results) == 0:
        print("No results to visualize.")
        return
    
    first_front = next(iter(results.values()))['front']
    is_3d = first_front.shape[1] >= 3
    
    # Color palette
    colors = [
        '#1f77b4', '#ff7f0e', '#2ca02c', '#d62728', '#9467bd',
        '#8c564b', '#e377c2', '#7f7f7f', '#bcbd22', '#17becf'
    ]
    
    if is_3d:
        fig = go.Figure()
        
        for idx, (problem_name, data) in enumerate(results.items()):
            front = data['front']
            color = colors[idx % len(colors)]
            
            # Add reference front if available
            if show_reference and data['reference'] is not None:
                ref = data['reference']
                fig.add_trace(go.Scatter3d(
                    x=ref[:, 0], y=ref[:, 1], z=ref[:, 2],
                    mode='markers',
                    marker=dict(size=2, color='lightgray', opacity=0.3),
                    name=f'{problem_name} (Reference)',
                    showlegend=True
                ))
            
            # Add obtained front
            fig.add_trace(go.Scatter3d(
                x=front[:, 0], y=front[:, 1], z=front[:, 2],
                mode='markers',
                marker=dict(size=4, color=color, opacity=0.8),
                name=f'{problem_name}',
                hovertemplate=(
                    f'<b>{problem_name}</b><br>'
                    'f₁: %{x:.4f}<br>'
                    'f₂: %{y:.4f}<br>'
                    'f₃: %{z:.4f}<extra></extra>'
                )
            ))
        
        fig.update_layout(
            title='Pareto Fronts (Interactive 3D)',
            scene=dict(
                xaxis_title='f₁',
                yaxis_title='f₂',
                zaxis_title='f₃',
                camera=dict(
                    up=dict(x=0, y=0, z=1),
                    center=dict(x=0, y=0, z=0),
                    eye=dict(x=1.5, y=1.5, z=1.5)
                )
            ),
            legend=dict(
                yanchor="top",
                y=0.99,
                xanchor="left",
                x=0.01
            ),
            margin=dict(l=0, r=0, b=0, t=40)
        )
    else:
        # 2D interactive plot
        fig = go.Figure()
        
        for idx, (problem_name, data) in enumerate(results.items()):
            front = data['front']
            color = colors[idx % len(colors)]
            
            # Add reference front if available
            if show_reference and data['reference'] is not None:
                ref = data['reference']
                sorted_ref = ref[ref[:, 0].argsort()]
                fig.add_trace(go.Scatter(
                    x=sorted_ref[:, 0], y=sorted_ref[:, 1],
                    mode='lines',
                    line=dict(color='lightgray', width=1),
                    name=f'{problem_name} (Reference)',
                    showlegend=True
                ))
            
            # Add obtained front
            fig.add_trace(go.Scatter(
                x=front[:, 0], y=front[:, 1],
                mode='markers',
                marker=dict(size=8, color=color, opacity=0.7),
                name=f'{problem_name}',
                hovertemplate=(
                    f'<b>{problem_name}</b><br>'
                    'f₁: %{x:.4f}<br>'
                    'f₂: %{y:.4f}<extra></extra>'
                )
            ))
        
        fig.update_layout(
            title='Pareto Fronts (Interactive)',
            xaxis_title='f₁',
            yaxis_title='f₂',
            legend=dict(
                yanchor="top",
                y=0.99,
                xanchor="left",
                x=0.01
            ),
            hovermode='closest'
        )
    
    if output_path:
        if output_path.endswith('.html'):
            fig.write_html(output_path)
        else:
            fig.write_image(output_path)
        print(f"Saved interactive plot to {output_path}")
    
    fig.show()


def visualize_interactive_grid(results: Dict[str, Dict[str, np.ndarray]],
                               output_path: Optional[str] = None,
                               show_reference: bool = True) -> None:
    """
    Create interactive grid visualization using Plotly subplots.
    
    Each problem gets its own subplot, useful for comparing individual fronts.
    """
    if not PLOTLY_AVAILABLE:
        print("Plotly not available. Install with: pip install plotly")
        return
    
    if len(results) == 0:
        print("No results to visualize.")
        return
    
    n_problems = len(results)
    n_cols = min(3, n_problems)
    n_rows = (n_problems + n_cols - 1) // n_cols
    
    first_front = next(iter(results.values()))['front']
    is_3d = first_front.shape[1] >= 3
    
    colors = [
        '#1f77b4', '#ff7f0e', '#2ca02c', '#d62728', '#9467bd',
        '#8c564b', '#e377c2', '#7f7f7f', '#bcbd22', '#17becf'
    ]
    
    if is_3d:
        # Create 3D subplots
        specs = [[{'type': 'scatter3d'} for _ in range(n_cols)] for _ in range(n_rows)]
        fig = make_subplots(
            rows=n_rows, cols=n_cols,
            subplot_titles=list(results.keys()),
            specs=specs,
            horizontal_spacing=0.05,
            vertical_spacing=0.1
        )
        
        for idx, (problem_name, data) in enumerate(results.items()):
            row = idx // n_cols + 1
            col = idx % n_cols + 1
            front = data['front']
            color = colors[idx % len(colors)]
            
            # Add reference front
            if show_reference and data['reference'] is not None:
                ref = data['reference']
                fig.add_trace(
                    go.Scatter3d(
                        x=ref[:, 0], y=ref[:, 1], z=ref[:, 2],
                        mode='markers',
                        marker=dict(size=2, color='lightgray', opacity=0.3),
                        name='Reference',
                        showlegend=(idx == 0)
                    ),
                    row=row, col=col
                )
            
            # Add obtained front
            fig.add_trace(
                go.Scatter3d(
                    x=front[:, 0], y=front[:, 1], z=front[:, 2],
                    mode='markers',
                    marker=dict(size=3, color=color, opacity=0.8),
                    name='Front',
                    showlegend=(idx == 0)
                ),
                row=row, col=col
            )
    else:
        # Create 2D subplots
        fig = make_subplots(
            rows=n_rows, cols=n_cols,
            subplot_titles=list(results.keys()),
            horizontal_spacing=0.08,
            vertical_spacing=0.12
        )
        
        for idx, (problem_name, data) in enumerate(results.items()):
            row = idx // n_cols + 1
            col = idx % n_cols + 1
            front = data['front']
            color = colors[idx % len(colors)]
            
            # Add reference front
            if show_reference and data['reference'] is not None:
                ref = data['reference']
                sorted_ref = ref[ref[:, 0].argsort()]
                fig.add_trace(
                    go.Scatter(
                        x=sorted_ref[:, 0], y=sorted_ref[:, 1],
                        mode='lines',
                        line=dict(color='lightgray', width=1),
                        name='Reference',
                        showlegend=(idx == 0)
                    ),
                    row=row, col=col
                )
            
            # Add obtained front
            fig.add_trace(
                go.Scatter(
                    x=front[:, 0], y=front[:, 1],
                    mode='markers',
                    marker=dict(size=6, color=color, opacity=0.7),
                    name='Front',
                    showlegend=(idx == 0)
                ),
                row=row, col=col
            )
    
    fig.update_layout(
        title='Pareto Fronts Grid (Interactive)',
        height=300 * n_rows,
        width=400 * n_cols,
        showlegend=True
    )
    
    if output_path:
        if output_path.endswith('.html'):
            fig.write_html(output_path)
        else:
            fig.write_image(output_path)
        print(f"Saved interactive grid to {output_path}")
    
    fig.show()


def main():
    parser = argparse.ArgumentParser(
        description='Visualize Pareto fronts from TrainingSet execution results.',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
    # Matplotlib visualizations
    python visualize_training_set_results.py results/ZDT --grid
    python visualize_training_set_results.py results/DTLZ3D --combined --save output.png
    
    # Plotly interactive visualizations (great for 3D rotation)
    python visualize_training_set_results.py results/RE3D --interactive
    python visualize_training_set_results.py results/DTLZ3D --interactive-grid --save fronts.html
        """
    )
    
    parser.add_argument('results_dir', help='Directory containing the results')
    parser.add_argument('--grid', action='store_true', 
                        help='Show grid of individual plots (matplotlib)')
    parser.add_argument('--combined', action='store_true', 
                        help='Show all fronts in one plot (matplotlib)')
    parser.add_argument('--interactive', action='store_true', 
                        help='Show interactive plot with all fronts (plotly)')
    parser.add_argument('--interactive-grid', action='store_true', 
                        help='Show interactive grid of plots (plotly)')
    parser.add_argument('--no-reference', action='store_true',
                        help='Hide reference fronts')
    parser.add_argument('--save', type=str, metavar='PATH', 
                        help='Save plot to file (.png, .pdf, .html)')
    parser.add_argument('--figsize', type=int, nargs=2, metavar=('W', 'H'), 
                        help='Figure size in inches (matplotlib only)')
    
    args = parser.parse_args()
    
    # Default to grid view if no visualization type specified
    if not (args.grid or args.combined or args.interactive or args.interactive_grid):
        args.grid = True
    
    # Load results
    print(f"Loading results from {args.results_dir}...")
    results = load_results(args.results_dir)
    
    if not results:
        print(f"No results found in {args.results_dir}")
        sys.exit(1)
    
    print(f"Found {len(results)} problem(s): {', '.join(results.keys())}")
    
    # Check dimensionality
    first_front = next(iter(results.values()))['front']
    print(f"Front dimensionality: {first_front.shape[1]}D")
    
    figsize = tuple(args.figsize) if args.figsize else None
    show_reference = not args.no_reference
    
    # Visualize
    if args.interactive:
        visualize_interactive(results, args.save, show_reference)
    elif args.interactive_grid:
        visualize_interactive_grid(results, args.save, show_reference)
    elif args.grid:
        visualize_grid(results, args.save, figsize)
    elif args.combined:
        visualize_combined(results, args.save, figsize)


if __name__ == '__main__':
    main()
